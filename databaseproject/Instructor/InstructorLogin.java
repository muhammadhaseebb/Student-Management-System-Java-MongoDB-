    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        email= jTextField2.getText();
        pass = jTextField1.getText();
        String dpt = (String) jComboBox1.getSelectedItem();
        try{
            MongoClient mongo = new MongoClient( "localhost" , 27017 );
                            //Connecting to the database
            MongoDatabase database = mongo.getDatabase("db_project");
                            //Creating a collection object
            MongoCollection<Document> mycol = database.getCollection("department");
            
            MongoIterable<Document> docs = mycol.find(new Document ("name", dpt));
            Document department = new Document(docs.first());
            
            List<Document> instructor_id = department.getList("instructor", Document.class);
            
            
            
            for(int i = 0; i < instructor_id.size(); i++){
                String inst_mail = instructor_id.get(i).getString("email");
                String inst_pass = instructor_id.get(i).getString("password");
                String inst_id = instructor_id.get(i).getString("_id");
                if (email.equals(inst_mail) && pass.equals(inst_pass)){
                        id = inst_id;
                        JOptionPane.showMessageDialog(f, "Login Successful","Logged in", JOptionPane.PLAIN_MESSAGE);
                        login = true;
                        selectSection log = new selectSection(id,dpt);  
                        log.setVisible(true);
                        dispose();
            }
        }
            if(!login) {
                    JOptionPane.showMessageDialog(f, "INCORRECT PASSWORD OR EMAIL","Alert", JOptionPane.WARNING_MESSAGE);
                }
        }            
     catch(Exception e)
        {
         System.out.print("Not connected");
        } 
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

